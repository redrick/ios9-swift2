import UIKit

func add(first: Double, second: Double) -> Double {
    return first + second
}

func subtract(first: Int, second: Int) -> Int {
    return first - second
}

func multiply(first: Float, second: Float) -> Float {
    return first * second
}

func divide(first: Double, second: Double) -> Double {
    return first / second
}

